    public void load (){
        model.setRowCount(0);
        try {
            for(Machine machine : dao.findMachinesBySalle((Salle) comboSalle.getSelectedItem())){
                model.addRow(new Object[]{
                    machine.getId(),
                    machine.getRef(),
                    machine.getMarque(),
                    machine.getPrix(),
                });
            }
        } catch (RemoteException ex) {
            Logger.getLogger(MachinesParSalle.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void populateCombo(){
        try {
            for(Salle s :daoS.findAll()){
                comboSalle.addItem(s);
            }
        } catch (RemoteException ex) {
            Logger.getLogger(MachinesParSalle.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
